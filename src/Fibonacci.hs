module Fibonacci
( fibs
 ) where

----------------------------------------------------------------------------------------------------
-- Exported functions
----------------------------------------------------------------------------------------------------
fibs :: [Integer]
fibs = fmap fib [1, 2 ..] 
 
----------------------------------------------------------------------------------------------------
-- Private functions
----------------------------------------------------------------------------------------------------        
fib :: Integer -> Integer
fib 1 = 1
fib 2 = 1
fib n = fib (n - 1) + fib (n - 2)